class idioma:

    def __init__(self, idioma: str, nivel: int, dificultad: str, precio:float):
        self.nivel = nivel
        self.dificultad = dificultad
        self.precio = precio
        self.idioma = idioma


    def imprimir_idioma(self):
        print(f"Idioma: {self.idioma}, Nivel: {self.nivel}, Dificultad: {self.dificultad}, Precio: {self.precio}")


        




       


    